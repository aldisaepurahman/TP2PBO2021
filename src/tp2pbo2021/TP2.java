    //buat fungsi koneksi ke database
    public static Connection connect(String host, String port, 
            String username, String password, String db){
        //buat koneksinya dengan library jdbc mysql
        String conn = "jdbc:mysql://"+ host + ":" + port + "/" + db;
        Connection koneksi;
        //coba untuk koneksi ke database
        try{
            //ambil library mysql
            Class.forName("com.mysql.cj.jdbc.Driver");
            //tes koneksinya
            koneksi = DriverManager.getConnection(conn, username, password);
        }catch(Exception ex){
            //jika koneksi gagal, tampilkan pesan error dan set koneksi dengan null
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Koneksi Error");
            koneksi = null;
        }
        //kembalikan koneksi
        return koneksi;
        
    }
    
    //buat prosedur mengambil data mobil dari database
    public void getMobil(){
        //try and catch karena ini diperlukan untuk mengecek koneksi database
        try{
            //buat koneksi sebelum mengeksekusi query
            Statement stmt = koneksi.createStatement();
            //ambil data mobil dari database
            String query = "SELECT * FROM mobil";
            //eksekusi querynya
            ResultSet rs = stmt.executeQuery(query);
            //lakukan perulangan selama ada data dari database
            while(rs.next()){
                //masukkan kedalam list Mobil
                listMobil.add(new Mobil(
                        rs.getString("merk"), rs.getString("plat"),
                        rs.getString("warna"), rs.getString("jenis")));
            }
        }catch(SQLException ex){
            //yang dilakukan bila koneksi gagal
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi Error Saudara-Saudara!");
        }
    }
    
    //buat prosedur menampilkan data dari database
    public void printMobil(){
        //ambil data dari database
        getMobil();
        //reset data dari table
        dtm.setRowCount(0);
        //tampilkan datanya pada table
        for (int i = 0; i < listMobil.size(); i++) {
            Object[] objs = {i+1, listMobil.get(i).getMerk(),
                listMobil.get(i).getPlat(), listMobil.get(i).getWarna(),
                listMobil.get(i).getJenis()};
            dtm.addRow(objs);
        }
        //hapus isi listMobil untuk mereset ulang hasilquery ketika dipanggil kembali
        listMobil.clear();
    }

