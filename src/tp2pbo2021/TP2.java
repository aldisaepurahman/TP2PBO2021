    //aksi tombol submit ketika diklik
    private void jButtonSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSubmitMouseClicked
        //ambil data inputan dan simpan kedalam variabel
        String merk = jTextFieldMerk.getText();
	String plat = jTextFieldPlat.getText();
        String warna = jTextFieldWarna.getText();
        String jenis = jComboBoxJenis.getSelectedItem().toString();
        //jika data yang diisi lengkap
        if (!warna.isEmpty() && !plat.isEmpty() && !merk.isEmpty()){
            //try and catch untuk insert data
            try{
                //buat koneksi untuk eksekusi query
                Statement stmt = koneksi.createStatement();
                //buat query insert data mobil
                String query = "INSERT INTO mobil (plat, merk, warna, jenis) "
                        + "VALUES ("
                        + "'"+ plat +"','"+ merk +"','"+ warna + "','"+ jenis + "')";
                //tampung status eksekusi querynya
                int sukses = stmt.executeUpdate(query);
                //jika status nya sukses
                if (sukses == 1) {
                    //tampilkan pesan sukses
                    JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan",
                            "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    //kosongkan data dari table
                    jTextFieldMerk.setText("");
                    jTextFieldPlat.setText("");
                    jTextFieldWarna.setText("");
                    jComboBoxJenis.setSelectedIndex(0);
                    //tampilkan kembali label info dan table
                    jLabelTable.setVisible(true);
                    jLabelInfo.setVisible(true);
                }
                //jika gagal, tampilkan pesan gagal
                else{
                    JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }catch(SQLException ex){
                //jika koneksi gagal, tampilkan pesan error
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Pada Database",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        //jika data form belum diisi sepenuhnya, tampilkan pesan error
        else{
            JOptionPane.showMessageDialog(null, "Pastikan Form Sudah Terisi Dengan Benar",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSubmitMouseClicked

